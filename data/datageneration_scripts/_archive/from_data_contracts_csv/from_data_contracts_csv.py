#!/usr/bin/env python3
"""
QuantLib Contract Executor
Reads contracts.csv generated by data_contracts_generator.py and evaluates each contract
using QuantLib. Outputs results.csv with pricing and risk metrics.
"""

import csv
import sys
from datetime import datetime
from typing import Dict, Any, Optional, List, Tuple

import QuantLib as ql

# ---------------------------
# Configuration
# ---------------------------
MARKET_YIELD_CURVES = {
    "EUR": 0.025,  # 2.5% flat risk-free rate
    "USD": 0.045,  # 4.5% flat
    "GBP": 0.035,  # 3.5% flat
}

DAY_COUNTER_MAP = {
    "30/360": ql.Thirty360(),
    "ACT/360": ql.Actual360(),
    "ACT/365": ql.Actual365Fixed(),
    "ACT/ACT": ql.ActualActual(),
}

FREQUENCY_MAP = {
    "monthly": ql.Monthly,
    "quarterly": ql.Quarterly,
    "semiannual": ql.Semiannual,
    "annual": ql.Annual,
}

BUSINESS_DAY_CONVENTION_MAP = {
    "Following": ql.Following,
    "ModifiedFollowing": ql.ModifiedFollowing,
    "Preceding": ql.Preceding,
}

CALENDAR = ql.TARGET()  # European calendar, adjust as needed


def parse_ql_date(date_str: str) -> ql.Date:
    """Convert ISO date string (YYYY-MM-DD) to QuantLib Date."""
    if not date_str:
        return ql.Date()
    dt = datetime.strptime(date_str, "%Y-%m-%d")
    return ql.Date(dt.day, dt.month, dt.year)


def get_flat_curve(currency: str, settlement_date: ql.Date) -> ql.YieldTermStructureHandle:
    """Create a flat yield curve for the given currency."""
    rate = MARKET_YIELD_CURVES.get(currency, 0.04)
    day_counter = ql.Actual360()
    curve = ql.FlatForward(settlement_date, rate, day_counter)
    return ql.YieldTermStructureHandle(curve)


